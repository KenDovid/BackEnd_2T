let opcion = parseInt(5);

switch (opcion) {
  case 1:
    console.log("Pizza");
    break;
  case 2:
    console.log("Hamburguesa");
    break;
  case 3:
    console.log("Tacos");
    break;
  case 4:
    console.log("Ensalada");
    break;
  case 5:
    console.log("Sushi");
    break;
  default:
    console.log("Opción no válida.");
}


