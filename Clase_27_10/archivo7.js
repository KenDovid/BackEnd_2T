let diasemana = 3;
let nombreDia;
switch (diasemana) {
    case 1:
        nombreDia = "Lunes"
        console.log(nombreDia)
        break;
    case 2:
        nombreDia = "Martes"
        console.log(nombreDia)
        break;
    case 3:
        nombreDia = "Miércoles"
        console.log(nombreDia)
        break;
    case 4:
        nombreDia = "Jueves"
        console.log(nombreDia)
        break;
    case 5:
        nombreDia = "Viernes"
        console.log(nombreDia)
        break;
    case 6:
        nombreDia = "Sábado"
        console.log(nombreDia)
        break;
    case 7:
        nombreDia = "Domingo"
        console.log(nombreDia)
        break;

    default:
        nombreDia = "Invalido"
        console.log(nombreDia)
        break;
}